
# In Python, variables are used to store and manage data. 
# There are several types of variables, each designed to hold different types of data. 
# Here are some of the main types of variables in Python:

# Integer (int):

# Represents whole numbers without any decimal point.
# Example: x = 5
# Float (float):

# Represents real numbers with a decimal point.
# Example: y = 3.14
# String (str):

# Represents sequences of characters (text).
# Example: name = "John"
# Boolean (bool):

# Represents a binary value, either True or False.
# Example: is_valid = True
# List:

# Represents an ordered collection of items. Elements can be of different data types.
# Example: fruits = ['apple', 'banana', 'orange']
# Tuple:

# Similar to a list but immutable, meaning the elements cannot be changed after the tuple is created.
# Example: coordinates = (3, 4)
# Set:

# Represents an unordered collection of unique elements.
# Example: unique_numbers = {1, 2, 3, 4}
# Dictionary (dict):

# Represents a collection of key-value pairs. Each key must be unique.
# Example: person = {'name': 'John', 'age': 25, 'city': 'New York'}
# NoneType (None):

# Represents the absence of a value or a null value.
# Example: result = None
# Complex (complex):

# Represents complex numbers with a real and imaginary part.
# Example: z = 3 + 2j
# These variable types allow Python to be a dynamically-typed language,
# meaning you don't need to explicitly declare the data type of a variable; 
# the interpreter determines the type dynamically based on the assigned value.

# Remember that Python is case-sensitive, so variables with different 
# capitalization are considered different (e.g., myVariable and myvariable are distinct). 
# Additionally, variable names should follow certain conventions, like starting with a letter or underscore, 
# and should not be a reserved keyword in Python.